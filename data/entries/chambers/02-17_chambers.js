  "body": 'The angel did not give Elijah a vision, or explain the Scriptures to him, or do anything remarkable; he told Elijah to do the most ordinary thing, viz., to get up and eat. If we were never depressed we should not be alive; it is the nature of a crystal never to be depressed.\nA human being is capable of depression, otherwise there would be no capacity for exaltation. There are things that are calculated to depress, things that are of the nature of death; and in taking an estimate of yourself, always take into account the capacity for depression.\nWhen the Spirit of God comes He does not give us visions, He tells us to do the most ordinary things conceivable. Depression is apt to turn us away from the ordinary commonplace things of God’s creation, but whenever God comes, the inspiration is to do the most natural simple thing - the things we would never have imagined God was in, and as we do them we find He is there. The inspiration which comes to us in this way is an initiative against depression; we have to do the next thing and do it in the inspiration of God. If we do a thing in order to overcome depression, we deepen the depression; but if the Spirit of God makes us feel intuitively that we must do the thing, and we do it, the depression is gone. Immediately we arise and obey, we enter on a higher plane of life.',
