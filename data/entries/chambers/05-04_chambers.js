  "body": 'Beware of imagining that intercession means bringing our personal sympathies into the presence of God and demanding that He does what we ask. Our approach to God is due entirely to the vicarious identification of our Lord with sin. We have “boldness to enter into the holiest by the blood of Jesus.”\nSpiritual stubbornness is the most effectual hindrance to intercession, because it is based on sympathy with that in ourselves and in others that we do not think needs atoning for. We have the notion that there are certain right and virtuous things in us which do not need to be based on the Atonement, and just in the domain of “stodge” that is produced by this idea we cannot intercede. We do not identify\nourselves with God’s interests in others, we get petulant with God; we are always ready with our own ideas, and intercession becomes the glorification of our own natural sympathies. We have to realize that the identification of Jesus with sin means the radical alteration of all our sympathies. Vicarious intercession means that we deliberately substitute God’s interests in others for our natural sympathy with them.\nAm I stubborn or substituted? Petted or perfect in my relationship to God? Sulky or spiritual? Determined to have my own way or determined to be identified with Him?',
